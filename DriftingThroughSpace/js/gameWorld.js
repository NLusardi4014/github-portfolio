// Class to simplify game world creation code 
// and allow for variable start positions
class gameWorld { 
	map = [];
	startPos = [];
	gameObj = [];

	constructor(levelNum) {
	this.map = gameworld["world" + levelNum];
	this.startPos = levelStartPos["level" + levelNum];
	this.gameObj = allGameObjects["level" + levelNum];
	}

	clearCurrent() {
		this.map = [];
		this.startPos = [];
		this.gameObj = [];
	}

	rebuildCurrent(levelNum) {
		this.map = gameworld["world" + levelNum];
		this.startPos = levelStartPos["level" + levelNum];
		this.gameObj = allGameObjects["level" + levelNum];
	}

	// For loadLevel(levelNum)
	rebuildObjects(levelNum) {
		this.gameObj = allGameObjects["level" + levelNum];
	}
}



